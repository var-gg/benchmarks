#!/usr/bin/env bash
#
# Deterministic "package a run into the repo" step. Given an already-authored run
# directory (manifest.json + results.json + README*.md + run.sh + harness files),
# this regenerates checksums + the index, enforces the size gate, and commits+pushes.
#
#   bash scripts/add-run.sh runs/2026-07-12-css-anchor-positioning-floating-ui
#
# The content (manifest/results/README) is authored per run; this handles only the
# mechanical, judgment-free tail so an unattended agent can call it reliably.
#
set -euo pipefail
cd "$(dirname "$0")/.."

RUN="${1:?usage: add-run.sh runs/<YYYY-MM-DD>-<slug>}"
RUN="${RUN%/}"
[ -d "$RUN" ] || { echo "no such run dir: $RUN"; exit 1; }
[ -f "$RUN/manifest.json" ] || { echo "missing $RUN/manifest.json"; exit 1; }

echo "==> checksums for $RUN"
( cd "$RUN"
  # Hash the committed harness + evidence (everything except generated/large files).
  files=$(git ls-files . 2>/dev/null | grep -vE 'checksums.txt$' || true)
  # Include not-yet-tracked authored files too.
  files=$(printf '%s\n%s\n' "$files" "$(ls -1 | grep -vE 'checksums.txt$|\.venv$|__pycache__')" | sort -u | grep -vE '^$')
  {
    echo "# sha256 of committed harness + evidence for this run."
    echo "# Regenerated by scripts/add-run.sh."
    echo
    # shellcheck disable=SC2086
    sha256sum $files 2>/dev/null || true
  } > checksums.txt
)

echo "==> regenerating index"
python scripts/gen-index.py

echo "==> size gate"
bash scripts/verify-size.sh

echo "==> commit + push"
git add -A
slug="$(basename "$RUN" | sed 's/^[0-9-]*//; s/^-//')"
if git diff --cached --quiet; then
  echo "nothing to commit."; exit 0
fi
git commit -q -m "Add run: ${slug}

Reproducible evidence for a var.gg firsthand experiment.

Co-Authored-By: Claude Opus 4.8 <noreply@anthropic.com>"
git push -q
echo "✅ pushed $RUN"
