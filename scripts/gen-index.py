#!/usr/bin/env python3
"""Regenerate the Runs table in README.md from every runs/*/manifest.json.
Deterministic: the index is a pure function of the run manifests, so adding a
run never requires hand-editing the table. Run from repo root or via add-run.sh.
"""
import json
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
README = ROOT / "README.md"
START = "<!-- RUNS:START (auto-generated by scripts/gen-index.py — do not edit by hand) -->"
END = "<!-- RUNS:END -->"


def post_links(post_url) -> str:
    # post_url may be a str or a {locale: url} dict.
    if isinstance(post_url, dict):
        return " · ".join(f"[{loc.upper()}]({url})" for loc, url in sorted(post_url.items()))
    if isinstance(post_url, str) and post_url:
        return f"[post]({post_url})"
    return "—"


def main() -> None:
    rows = []
    for mf in sorted(ROOT.glob("runs/*/manifest.json")):
        m = json.loads(mf.read_text(encoding="utf-8"))
        run_dir = mf.parent.name
        date = (m.get("run_id", run_dir)[:10]) or run_dir[:10]
        backfilled = " (backfill)" if m.get("backfilled") else ""
        rows.append((
            date,
            f"| {date} | [{run_dir}](./runs/{run_dir}/) | {post_links(m.get('post_url'))} | {m.get('kind', '—')}{backfilled} |",
        ))
    rows.sort(key=lambda r: r[0], reverse=True)

    table = ["| Date | Run | Post | Kind |", "|---|---|---|---|"] + [r[1] for r in rows]
    block = START + "\n" + "\n".join(table) + "\n" + END

    text = README.read_text(encoding="utf-8")
    new = re.sub(re.escape(START) + r".*?" + re.escape(END), block, text, count=1, flags=re.S)
    if new == text and START not in text:
        raise SystemExit("gen-index: RUNS markers not found in README.md")
    README.write_text(new, encoding="utf-8", newline="\n")
    print(f"gen-index: wrote {len(rows)} run(s) to README.md")


if __name__ == "__main__":
    main()
